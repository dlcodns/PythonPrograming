# 정수 n이 주어졌을 때, n을 1, 2, 3의 합으로 나타내는 방법의 
# 수를 구하는 프로그램을 작성하시오.

#생각
# n = 1
# 1 => 1개
# n = 2
# 1+1 / 2 => 2개
# n = 3
# 1+1+1 / 1+2 / 2+1 / 3 => 4개
# n = 4
# 1+1+1+1 / 1+1+2 / 1+2+1 / 2+1+1 / 2+2 / 1+3 / 3+1 => 7개
# 1+3 // 1+1+2 / 2+2 // 1+1+1+1 / 1+2+1 / 2+1+1 / 3+1 =>7개
# n =5
# 1+1+1+1+1 / 1+1+1+2 / 1+1+2+1 / 1+2+1+1 / 2+1+1+1 / 1+2+2 /
# 2+1+2 / 2+2+1 / 1+1+3 / 1+3+1 / 3+1+1 / 2+3 / 3+2 => 13개
# 1+1+3 / 2+3 // 1+1+1+2 / 1+2+2 / 2+1+2 / 3+2 //
# 1+1+1+1+1 / 1+1+2+1 / 1+2+1+1 / 2+1+1+1 / 2+2+1 / 1+3+1 / 3+1+1 => 13개
# 
# [n-1], [n-2], [n-3]의 값을 모두 합한 게 [n]이다.

case = int(input())
dp = [0]*12
dp[1] = 1
dp[2] = 2
dp[3] = 4

for i in range (4, 12):
    dp[i] = dp[i-1]+dp[i-2]+dp[i-3]

for i in range(0,case):
    n = int(input())
    print(dp[n])